package tutorial1;

public class forloop {
public static void main(String[] args) {
	// let's play with for loop
	// it is same as while loop 
	for(int i=0;i<5;i++)
	{  // you can iterate anything 
		// all condition is in for box
		System.out.printf("the value of i is : %d\n",i);
	// printf is use for including format specifier if want 
		// else you can use println
	}
}
}
